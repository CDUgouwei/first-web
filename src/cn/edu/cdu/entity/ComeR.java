package cn.edu.cdu.entity;

public class ComeR {
	private int id;
	private int bumen;
	private String positon;
	private int salary;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBumen() {
		return bumen;
	}
	public void setBumen(int bumen) {
		this.bumen = bumen;
	}
	public String getPositon() {
		return positon;
	}
	public void setPositon(String positon) {
		this.positon = positon;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	
}
